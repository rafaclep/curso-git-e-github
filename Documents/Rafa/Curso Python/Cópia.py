#Programa que peça dois números, realize as principais operações soma, subtração, multiplicação, divisão
print ('Realize Operações Matemáticas')
number1 = float(input('Digite o primeiro número: '))
number2 = float(input(' Digite o segundo número: '))

#Operações matemáticas
soma = number1 + number2
subtr = number1 - number2
mult = number1 * number2
div = number1 / number2

#Resultado das operações
print(f'Soma = {number1} + {number2} = {soma}')
print(f'Subtração = {number1} - {number2} = {subtr}')
print(f'Multiplicação = {number1} * {number2} = {mult}')
print(f'Divisão = {number1} / {number2} = {div}')






